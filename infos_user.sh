infos_user.sh
Les administrateurs système écrivent souvent des scripts pour automatiser certaines tâches. 
Donnez quelques exemples de situations où de tels scripts sont utiles.

Écrivez un script qui, lors de son exécution, donne la date et l'heure, 
la liste de tous les utilisateurs connectés et 
le temps passé depuis le lancement du système (uptime) du système. 
Pour terminer, votre script doit enregistrer cette information dans un journal.

